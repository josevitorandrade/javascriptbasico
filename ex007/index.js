
//como somar valores no javascript




let primeiroValor = 4
let segundoValor = 2
// se colocar o segundo valor como string por exemplo entre aspas, o java script ira juntar os dois valores dando o resultado de 42.
//soma 
console.log(primeiroValor + segundoValor)
//subtração
console.log(primeiroValor - segundoValor)
//divisao
console.log(primeiroValor / segundoValor)
//multiplicação
console.log(primeiroValor * segundoValor)
//porcentagem ou resto da divisao
console.log(primeiroValor % segundoValor)


